# 문자열의 길이를 반환
string_example = "Hello, World!"
length_of_string = len(string_example)
print("문자열의 길이:", length_of_string)

# 리스트의 길이를 반환
list_example = ["1", 2, 3, 4, 5]
length_of_list = len(list_example)
print("리스트의 길이:", length_of_list)

# 튜플의 길이를 반환
tuple_example = (10, 20, 30, 40)
length_of_tuple = len(tuple_example)
print("튜플의 길이:", length_of_tuple)
